xor_keys = [0x78, 0x77, 0x12, 0xB7, 0xCD, 0x1E, 0xE9, 0xD4, 0x0E, 0xE3,
            0x60, 0xBB, 0xE2, 0x27, 0x8E, 0x9A, 0xEB, 0x1B, 0x41, 0x4A,
            0xB7, 0x53, 0x0A]

expected_values = [0x2B, 0x07, 0x73, 0xC5, 0xA6, 0x65, 0x91, 0xE4, 0x7C, 0xBC,
                  0x09, 0x8E, 0xBD, 0x49, 0xBE, 0xEE, 0xB4, 0x48, 0x75, 0x2C,
                  0x84, 0x2E]


def xor_operation(xor_keys, expected_values):
    result = []
    for key, expected in zip(xor_keys, expected_values):
        result.append(key ^ expected)
    return result


def xor_to_char(xor_result):
    return ''.join(chr(val) if 32 <= val <= 126 else '.' for val in xor_result)
print(xor_to_char(xor_operation(xor_keys, expected_values)))
